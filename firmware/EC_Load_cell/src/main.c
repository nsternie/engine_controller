#define adc0 50
#define adc1 51
#define adc2 52
#define adc3 53
#define adc4 54

#define sram 60

#define millis	((__HAL_TIM_GET_COUNTER(&htim2))/1000)


uint16_t adc_data[5][16];		// 5 ADCs, 16 channels each.
float load[6];
float thrust_load;

#define SLOPE 0
#define OFFSET 1
float load_cal[2][6] = {{
		// SLOPES
		0.5,
		0.5,
		0.5,
		0.5,
		1,
		1,
},{
		// OFFSETS
		0,
		0,
		0,
		0,
		0,
		0,
}};

void set_device(uint8_t device, GPIO_PinState state){
	switch(device){
		case adc0:
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_12, state);
			break;
		case adc1:
			HAL_GPIO_WritePin(GPIOE, GPIO_PIN_15, state);
			break;
		case adc2:
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_0, state);
			break;
		case adc3:
			HAL_GPIO_WritePin(GPIOE, GPIO_PIN_14, state);
			break;
		case adc4:
			HAL_GPIO_WritePin(GPIOE, GPIO_PIN_4, state);
			break;

		case sram:
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_14, state);
			break;


		default:
			break;
	}
}
void select_device(uint8_t device){
	set_device(device, GPIO_PIN_RESET);
}
void release_device(uint8_t device){
	set_device(device, GPIO_PIN_SET);
}
void print_adc_raw(UART_HandleTypeDef device){

	uint8_t line[255];

	snprintf(line, sizeof(line), "\f\r\nChannel\tADC0\tADC1\tADC2\tADC3\tADC4\r\n", adc_data);
	while(HAL_UART_GetState(&device) == HAL_UART_STATE_BUSY_TX);
	HAL_UART_Transmit(&device, (uint8_t*)line, strlen(line), 1);

	for(uint8_t channel = 0; channel <= 15; channel++){
		HAL_Delay(40);
		snprintf(line, sizeof(line), "%d:\t%d\t%d\t%d\t%d\t%d\t\r\n", channel, adc_data[0][channel], adc_data[1][channel], adc_data[2][channel], adc_data[3][channel], adc_data[4][channel]);
		while(HAL_UART_GetState(&huart1) == HAL_UART_STATE_BUSY_TX);
		HAL_UART_Transmit(&device, (uint8_t*)line, strlen(line), 1);
	}


}
// Hardware wrappers
void read_adc(SPI_HandleTypeDef* SPI_BUS){

	// Temp Buffers
	uint8_t tx[2];
	uint8_t rx[2];

	 __disable_irq();

	uint8_t adcn = adc3;

		// Select channel 0 to start
		tx[0] = 0;
		tx[1] = 0b0000100;
		select_device(adcn);
		if(HAL_SPI_TransmitReceive(SPI_BUS, tx, rx, 2, 0xffff) ==  HAL_TIMEOUT){
		}
		release_device(adcn);

		for(uint8_t channel = 1; channel <= 16; channel++){

			tx[0] = (channel >> 1) | 0b00001000;
			tx[1] = (channel << 7) | 0b00000100;
			select_device(adcn);
			if(HAL_SPI_TransmitReceive(SPI_BUS, tx, rx, 2, 0xffff) == HAL_TIMEOUT){

			}

			release_device(adcn);
			adc_data[adcn-adc0][channel-1] = ((rx[1])|(rx[0] << 8)) & 0x0FFF;

		}

	 __enable_irq();
}


void scale_readings(){
	load[0] = adc_data[3][15];
	load[1] = adc_data[3][14];
	load[2] = adc_data[3][13];
	load[3] = adc_data[3][12];
	for(uint8_t n = 0; n < 6; n++){
		load[n] -= load_cal[OFFSET][n];
		load[n] *= load_cal[SLOPE][n];
	}
	thrust_load = load[0]+load[1]+load[2]+load[3];
}
